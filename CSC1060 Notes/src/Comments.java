
public class Comments {
	
	public static void main(String[] args) {
		
		// This is a single line comment
		
		/*
		 * This is a multi-line comment
		 */
		
		System.out.print("This is just print\n"); // The \n is an escape character indicating a new line
		System.out.println("This is print line");
		
		/*
		 * ESCAPE SEQUENCES:
		 * \n - New Line
		 * \t - Tab
		 * \" Double Quote
		 * \\ - Backslash
		 */
	}

}
