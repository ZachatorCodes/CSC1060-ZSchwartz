
public class ThreeLine {
	public static void main(String[] args) {
		System.out.println("First");
		printLines();
		System.out.println("Second");
		printLines();
		System.out.println("Third");
		printLines();
		System.out.println("Fourth");
	}

	public static void printLines() {
		System.out.println();
		System.out.println();
		System.out.println();
	}
}
